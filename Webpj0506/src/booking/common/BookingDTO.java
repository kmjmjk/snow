package booking.common;

public class BookingDTO {
	private String bkid, bkstylist, bktime, bkservice, bkvisitdate;
	private int bknum, bktcost, bkdtime;
	
	public String getBkid() {
		return bkid;
	}
	public void setBkid(String bkid) {
		this.bkid = bkid;
	}
	public String getBkstylist() {
		return bkstylist;
	}
	public void setBkstylist(String bkstylist) {
		this.bkstylist = bkstylist;
	}
	public String getBktime() {
		return bktime;
	}
	public void setBktime(String bktime) {
		this.bktime = bktime;
	}
	public String getBkservice() {
		return bkservice;
	}
	public void setBkservice(String bkservice) {
		this.bkservice = bkservice;
	}
	public String getBkvisitdate() {
		return bkvisitdate;
	}
	public void setBkvisitdate(String bkvisitdate) {
		this.bkvisitdate = bkvisitdate;
	}
	public int getBknum() {
		return bknum;
	}
	public void setBknum(int bknum) {
		this.bknum = bknum;
	}
	public int getBktcost() {
		return bktcost;
	}
	public void setBktcost(int bktcost) {
		this.bktcost = bktcost;
	}
	public int getBkdtime() {
		return bkdtime;
	}
	public void setBkdtime(int bkdtime) {
		this.bkdtime = bkdtime;
	}
	

}
